//Expresión
let a = 5;
let b = 9;

console.log("a = ", a);
console.log("b = ", b);

console.log("\nOperaciones aritméticas:");
// a+b es una declaración
console.log(a+b, 'adición');
console.log(a-b, 'sustracción');
console.log(a*b, 'multiplicación');
console.log(a/b, 'división');
console.log(a%b, 'módulo');
console.log(a**b, 'exponenciación');

console.log("\nincremento de a:");
console.log(++a, "Suma antes de mostrar");
console.log(a++, "Suma despues de mostrar");
console.log(a, "Resultado de la suma anterior");



console.log("\ndecremento de b:");
console.log(--b, "Resta antes de mostrar");
console.log(b--, "Resta despues de mostrar");
console.log(b, "Resultado de la resta anterior");