console.log(a > 5);
console.log(a < 5);
console.log(a >= 5);
console.log(a <= 5);

console.log("\nOperadores de igualdad:");
console.log("\n> Compara solo VALOR:");
console.log(a == 10);
console.log(a != 10);
console.log(a == "10");
console.log(a != "10");

console.log("\n> Compara VALOR & TIPO:");
console.log(a === 10);
console.log(a !== 10);
console.log(a === '10');
console.log(a !== '10');